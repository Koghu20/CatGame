    if(username.getText().isEmpty()||email.getText().isEmpty()||password.getText().isEmpty()||rpassword.getText().isEmpty()||name.getText().isEmpty())
      JOptionPane.showMessageDialog(null, "Please enter all fields");
      else if(email.getText().contains("@")==false || email.getText().contains(".com")==false)
       JOptionPane.showMessageDialog(null, "Please enter valid email");   
     else if(!password.getText().equals(rpassword.getText()))
          JOptionPane.showMessageDialog(null, "password doesnot match");
     else if(password.getText().length()<8)
        JOptionPane.showMessageDialog(null, "password should be atleast 8 characters");
      else
      {   
          String msg=password.getText();
          write();
          new acc_creation(msg).setVisible(true);
          close();
      }
